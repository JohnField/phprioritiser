<?php

namespace PHPrioritiser;

/**
 * PHPrioritiser
 *
 * @author John Field <John.Field@gMail.com>
 */
class PHPrioritiser
{
    /**
     * Setter.
     *
     * @param mixed $foo
     */
    public function setFoo($foo)
    {
        $this->foo = $foo;
    }

    /**
     * Getter.
     *
     * @return mixed
     */
    public function getFoo()
    {
        return $this->foo;
    }
}
